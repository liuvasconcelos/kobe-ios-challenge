import UIKit

extension UIColor {
    @nonobjc public class var veryDarkGray: UIColor {
        return UIColor(red: 24/255, green: 24/255, blue: 24/255, alpha: 1)
    }
    
    @nonobjc public class var darkMode: UIColor {
        return UIColor(red: 20/255, green: 20/255, blue: 20/255, alpha: 1)
    }
}
